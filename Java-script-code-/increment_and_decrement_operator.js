// In js we have increment and decrement oprator they both have there own two 
// incrment => prefix and postfix increment 
// decrement => prefix and postfix decrement 

{
    // prefix
    let number = 55;
     let newnumb = ++number; // increment first the in return  return means assign 
     console.log(newnumb);
    console.log(number);
}
{
    //postfix
    let number = 55;
     let newnumb = number++; // return first increment after return means assign 
     console.log(newnumb);
    console.log(number);
}
// decrement 
{
    // prefix
    let number = 55;
     let newnumb = --number; // decrement first the in return  return means assign 
     console.log(newnumb);
    console.log(number);
}
{
    //postfix
    let number = 55;
     let newnumb = number--; // return first decrement after return means assign 
     console.log(newnumb);
    console.log(number);
}
